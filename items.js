// POTION.JS
function Potion(name, price, heal) {
  this.name = name;
  this.price = price;
  this.heal = heal;
}
// WEAPON.JS
function Weapon(name, price, minDmg, maxDmg) {
  this.name = name;
  this.price = price;
  this.minDmg = minDmg;
  this.maxDmg = maxDmg;
}
// ARMOR.JS
function Armor(name, price, defense) {
  this.name = name;
  this.price = price;
  this.defense = defense;
}